Je trekt het niet zonder aanhangerrijbewijs!
Caravan gekocht? Staan er wat grotere verhuizingen voor de deur? Vroeger kreeg je bij autorijbewijs (Rijbewijs B) automatisch een stempel voor het aanhangerrijbewijs (Rijbewijs E). Dat is niet meer.

Met Rijbewijs B mogen nog wel kleinere aanhangers, zoals bagagekarretjes worden getrokken, maar grotere niet. Denk hierbij aan tandemassers en de meeste caravans. De eerste categorie heeft een witte kentekenplaat identiek aan de auto, de tweede een gele plaat met eigen kenteken. Voor deze categorie is een Rijbewijs E vereist.
Inschrijven lessen BE rijbewijs
Welke aanhanger kan ik trekken?
Voor het trekken van een aanhanger met Rijbewijs B geldt kort door de bocht dat:

De aanhangwagen in totaal (massa leeg voertuig + laadvermogen) niet zwaarder mag zijn dan 750 kg;
De aanhangwagen zwaarder mag zijn dan 750 kg op voorwaarde dat de toegestane maximum massa van de totale combinatie (trekkend voertuig plus aanhangwagen) niet meer dan 3500 kg is.
In beide gevallen mag het werkelijk te trekken gewicht niet zwaarder zijn dan het trekvermogen van de auto. Wat dit vermogen is, staat in de kentekenpapieren.
